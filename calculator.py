import time, os

print('---------------------------------------------------------------------')
print('----------------------------CALCULATOR-------------------------------')
while True:
    print('---------------------------------------------------------------------')
    print('------------------------------CHOICE---------------------------------')
    print('----------_______-------_______-------_______-------_______----------')
    print('---------|   +   |-----|   -   |-----|   /   |-----|   *   |---------')
    print('---------------------------------------------------------------------')
    print('-------------------------INPUT-YOUR-CHOICE!--------------------------')
    print('---------------------------------------------------------------------')
    var = input('------------------|   ')
    if var != '+' and var != '-' and var != '/' and var != '*':
        print('------------PRINT:   + ,  - ,  /  or  *   ')
    else:
        print('---------------------------------------------------------------------')
        print('---------------------INPUT-YOUR-FIRST-NUMBER-------------------------')
        print('---------------------------------------------------------------------')
        first_numb = input('------------------|   ')
        print('---------------------------------------------------------------------')
        print('--------------------INPUT-YOUR-SECOND-NUMBER-------------------------')
        print('---------------------------------------------------------------------')
        second_numb = input('------------------|   ')

        if var == '/' and second_numb == '0':
            print('---------------------------------------------------------------------')
            print('-----------------------------!ERROR!---------------------------------')
            print('---------------------------------------------------------------------')
        elif var == '+':
            print('----------------------------ANSWER-------------------------------')
            print('-------|     ', int(first_numb) + int(second_numb), '   |-----------')
            print('-----------------------------------------------------------------')
        elif var == '-':
            print('----------------------------ANSWER-------------------------------')
            print('-------|     ', int(first_numb) - int(second_numb), '   |-----------')
            print('-----------------------------------------------------------------')
        elif var == '/':
            print('----------------------------ANSWER-------------------------------')
            print('-------|     ', int(first_numb) / int(second_numb), '   |-----------')
            print('-----------------------------------------------------------------')
        elif var == '*':
            print('----------------------------ANSWER-------------------------------')
            print('-------|     ', int(first_numb) * int(second_numb), '   |-----------')
            print('-----------------------------------------------------------------')

        time.sleep(1)